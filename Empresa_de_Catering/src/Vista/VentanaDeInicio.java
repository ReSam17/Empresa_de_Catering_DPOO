    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        VentanaNuevoCliente dialog = new VentanaNuevoCliente(this, true);
        
       final boolean acepted = dialog.exec();
       if(acepted){
          final Cliente cliente = dialog.getCliente();
          if(cliente != null){
              this.gp.getClientes().add(cliente);
              try {
                  gp.GuardarClientes("Archivos\\Clientes.txt");
              } catch (IOException ex) {
                  Logger.getLogger(VentanaDeInicio.class.getName()).log(Level.SEVERE, null, ex);
              }
              ActualizarTabla();
          }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            VentanaEliminarCliente dialog = new VentanaEliminarCliente(null, true);
            
            final boolean acepted = dialog.exec();
            if(acepted){
                try {
                    String s = dialog.getS().trim();
                    gp.deleteCleinte(s);
                    gp.GuardarClientes("Archivos\\Clientes.txt");
                } catch (IOException ex) {
                    Logger.getLogger(VentanaDeInicio.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }
            ActualizarTabla();
    }//GEN-LAST:event_jButton3ActionPerformed

