    private void ActualizarTabla(){
        String [] columnas = {"Nombre", "Email"};
        DefaultTableModel model = new DefaultTableModel(columnas,0);
        
        for(int i = 0;i < this.gp.getClientes().size(); i++){
            String nombre = this.gp.getClientes().get(i).getNombre();
            String email = this.gp.getClientes().get(i).getEmail();
            
            String [] fila = {nombre, email};
            model.addRow(fila);
        }
        
        jTableClientes.setModel(model);
    }
